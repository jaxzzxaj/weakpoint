package ex5_1.entity;

public class Trainee {
	private int id;
	private String name;
	private int age;
	private String gender;
	private int unitId;
	private int coId;

	/**
	 * @return id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id セットする id
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name セットする name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * @param age セットする age
	 */
	public void setAge(int age) {
		this.age = age;
	}

	/**
	 * @return gender
	 */
	public String getGender() {
		return gender;
	}

	/**
	 * @param gender セットする gender
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}

	/**
	 * @return unitId
	 */
	public int getUnitId() {
		return unitId;
	}

	/**
	 * @param unitId セットする unitId
	 */
	public void setUnitId(int unitId) {
		this.unitId = unitId;
	}

	/**
	 * @return coId
	 */
	public int getCoId() {
		return coId;
	}

	/**
	 * @param coId セットする coId
	 */
	public void setCoId(int coId) {
		this.coId = coId;
	}

}
